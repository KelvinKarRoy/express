//毫秒转换2011-12-12
function changeTime(date){
    var date = new Date(date);
    Y = date.getFullYear() + '-';
    M = (date.getMonth()+1 < 10 ? '0'+(date.getMonth()+1) : date.getMonth()+1) + '-';
    D = date.getDate() < 10 ? '0' + date.getDate() : date.getDate();
    return Y+M+D;
}

